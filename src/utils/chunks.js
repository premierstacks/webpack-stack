export function chunks(env, argv, config) {
  config.optimization = config.optimization || {};
  config.optimization.runtimeChunk = 'single';
  config.optimization.splitChunks = {
    chunks: 'all',
    cacheGroups: {
      vendor: {
        test: /[\\/]node_modules[\\/]/,
        name: 'vendors',
        chunks: 'all',
      },
    },
  };

  return config;
}
